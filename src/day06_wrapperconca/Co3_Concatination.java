package day06_wrapperconca;

public class Co3_Concatination {
    public static void main(String[] args) {
        String a="hello";
        int b=2;
        int c=3;
        System.out.println(a+b+c);
        System.out.println(c+b+a);
        System.out.println(a+(b+c));
        System.out.println(a+b*c);

    }
}
